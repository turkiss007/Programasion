package Bucles;

public class E3_Pares_entre_1_50 {
	public static void main(String[] args) {
		/*
		 * Imprimir números pares entre 1 y 50 Crea un programa que imprima todos los
		 * números pares entre 1 y 50 utilizando un bucle.
		 */
		System.out.println("Los 50 primeros numeros pares: ");
		for (int i = 2; i < 50; i = i + 2) {
			System.out.println(i);
		}
	}
}
