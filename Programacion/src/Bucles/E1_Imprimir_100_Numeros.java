package Bucles;

public class E1_Imprimir_100_Numeros {

	public static void main(String[] args) {
		/*
		 * Imprimir números del 1 al 100 Escribe un programa que imprima los números del
		 * 1 al 100 utilizando un bucle.
		 */
		System.out.print("Los 100 primeros números:");
		for (int i = 1; i < 101; i++) {
			System.out.print(i + ", ");
		}
	}

}
